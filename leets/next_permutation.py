    # @param A : list of integers
    # @return a list of integers
    def nextPermutation(self, A):
        N = len(A)

        index = -1
        for i in range(N-1, 0, -1):
            if A[i] > A[i-1]:
                index = i - 1
                break
            
        print(index)
        if index != -1:
            for i in range(N-1, index, -1):
                if A[i] > A[index]:
                    A[i], A[index] = A[index], A[i]
                    break

        i = index + 1
        j = N-1

        while i < j:
            A[i], A[j] = A[j], A[i]
            i += 1
            j -= 1
        
        return A

