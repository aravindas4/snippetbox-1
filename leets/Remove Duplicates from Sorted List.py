# Definition for singly-linked list.
# class ListNode:
#	def __init__(self, x):
#		self.val = x
#		self.next = None

class Solution:
	# @param A : head node of linked list
	# @return the head node in the linked list
    def deleteDuplicates(self, A):

        p = None
        c = A

        while c:
            t = c
            while t.next and t.val == t.next.val:
                t = t.next

            if t.next:
                c.next = t
            else:
                c.next = None

            c = c.next
        return A

